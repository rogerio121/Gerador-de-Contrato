package br.com.geracontrato.model;

public class Imovel {
	
	int idImovel;
	int numero;
	int comodos;
	double valorEmNumero;
	String rua;
	String bairro;
	String cidade;
	String uf;
	String cep;
	String valorExtenso;
	String cpfUsuario;
	
	
	
	public int getIdImovel() {
		return idImovel;
	}
	public void setIdImovel(int idImovel) {
		this.idImovel = idImovel;
	}
	
	
	public int getNumero() {
		return numero;
	}
	public void setNumero(int numero) {
		this.numero = numero;
	}
	
	
	public int getComodos() {
		return comodos;
	}
	public void setComodos(int comodos) {
		this.comodos = comodos;
	}
	
	
	public double getValorEmNumero() {
		return valorEmNumero;
	}
	public void setValorEmNumero(double valorEmNumero) {
		this.valorEmNumero = valorEmNumero;
	}
	
	
	public String getRua() {
		return rua;
	}
	public void setRua(String rua) {
		this.rua = rua;
	}
	
	
	public String getBairro() {
		return bairro;
	}
	public void setBairro(String bairro) {
		this.bairro = bairro;
	}
	
	
	public String getCidade() {
		return cidade;
	}
	public void setCidade(String cidade) {
		this.cidade = cidade;
	}
	
	
	public String getUf() {
		return uf;
	}
	public void setUf(String uf) {
		this.uf = uf;
	}
	
	
	public String getCep() {
		return cep;
	}
	public void setCep(String cep) {
		this.cep = cep;
	}
	
	
	public String getValorExtenso() {
		return valorExtenso;
	}
	public void setValorExtenso(String valorExtenso) {
		this.valorExtenso = valorExtenso;
	}
	
	
	public String getCpfUsuario() {
		return cpfUsuario;
	}
	public void setCpfUsuario(String cpfUsuario) {
		this.cpfUsuario = cpfUsuario;
	}
	
}
